package ru.job4j.strategy;

/**
 * Интерфейс {@code Shape} определяет контракт для всех геометрических фигур, которые могут быть отрисованы.
 *
 * <p>Используется в шаблоне проектирования "Стратегия" для реализации различных фигур,
 * таких как {@link Square}, {@link Triangle} и другие.</p>
 *
 * <p>Каждая реализация должна переопределить метод {@link #draw()}, возвращающий строковое
 * представление фигуры в псевдографике.</p>
 *
 * <p><b>Пример использования:</b></p>
 * <pre>{@code
 * Shape shape = new Square();
 * System.out.print(shape.draw());
 * }</pre>
 *
 * <p><b>Пример вывода:</b></p>
 * <pre>{@code
 *  _______
 * |       |
 * |       |
 * |_______|
 * }</pre>
 *
 * @author Maksim Merkulov
 * @version 1.1
 */
public interface Shape {

    /**
     * Возвращает строковое представление фигуры в псевдографике.
     *
     * <p>Каждая реализация формирует свой уникальный рисунок.</p>
     *
     * @return строка с изображением фигуры
     */
    String draw();
}
