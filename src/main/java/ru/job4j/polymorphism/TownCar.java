package ru.job4j.polymorphism;

/**
 * Класс {@code TownCar} представляет собой реализацию легкового автомобиля,
 * использующего автоматическую коробку передач и стандартные механизмы управления.
 *
 * <p>Реализует интерфейс {@link Vehicle}, предоставляя конкретные реализации методов
 * ускорения, торможения, управления, переключения передач и заправки топливом.</p>
 *
 * <p><b>Пример использования:</b></p>
 * <pre>{@code
 * Vehicle car = new TownCar();
 * car.changeGear();
 * car.accelerate();
 * car.steer();
 * car.brake();
 * car.refill();
 * }</pre>
 *
 * <p><b>Пример вывода:</b></p>
 * <pre>{@code
 * Коробка передач автомат. Поставить селектор в режим D.
 * Нажатие на педаль газа механически открывает дроссельную заслонку
 * Стандартное рулевое управление
 * Стандартная тормозная система
 * Заправить 60л бензина
 * }</pre>
 *
 * @author Maksim Merkulov
 * @version 1.0
 */
public class TownCar implements Vehicle {

    /**
     * Переключает коробку передач в режим движения.
     *
     * <p>Используется автоматическая трансмиссия:
     * селектор переводится в режим {@code D}.</p>
     */
    @Override
    public void changeGear() {
        System.out.println("Коробка передач автомат. Поставить селектор в режим D.");
    }

    /**
     * Ускоряет автомобиль за счет открытия дроссельной заслонки.
     *
     * <p>Осуществляется механическим соединением педали газа с заслонкой.</p>
     */
    @Override
    public void accelerate() {
        System.out.println("Нажатие на педаль газа механически открывает дроссельную заслонку");
    }

    /**
     * Управляет направлением движения автомобиля.
     *
     * <p>Используется стандартное рулевое управление.</p>
     */
    @Override
    public void steer() {
        System.out.println("Стандартное рулевое управление");
    }

    /**
     * Останавливает или замедляет автомобиль.
     *
     * <p>Применяется стандартная тормозная система с гидравлическим приводом.</p>
     */
    @Override
    public void brake() {
        System.out.println("Стандартная тормозная система");
    }

    /**
     * Заправляет автомобиль топливом.
     *
     * <p>Заправка производится бензином объемом 60 литров.</p>
     */
    @Override
    public void refill() {
        System.out.println("Заправить 60л бензина");
    }
}