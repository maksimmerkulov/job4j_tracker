package ru.job4j.ooa;

/**
 * Интерфейс {@code Shape} представляет собой общую абстракцию для всех фигур,
 * предоставляя методы для отображения и вычисления площади.
 *
 * <p>Каждая фигура, реализующая этот интерфейс, должна поддерживать два основных метода:
 * 1. {@link #draw()} — для вывода изображения фигуры в текстовом формате.
 * 2. {@link #square()} — для вычисления площади фигуры.</p>
 *
 * <p><b>Пример использования:</b></p>
 * <pre>{@code
 * Shape rectangle = new Rectangle(4, 3);
 * System.out.println(rectangle.draw());
 * System.out.println("Площадь: " + rectangle.square());
 * }</pre>
 *
 * <p><b>Пример вывода:</b></p>
 * <pre>{@code
 * ******
 * *    *
 * *    *
 * ******
 * Площадь: 12.0
 * }</pre>
 *
 * @author Maksim Merkulov
 * @version 1.0
 */
public interface Shape {

    /**
     * Рисует фигуру в текстовом представлении.
     *
     * @return строка, представляющая изображение фигуры
     */
    String draw();

    /**
     * Вычисляет площадь фигуры.
     *
     * @return площадь фигуры
     */
    double square();
}