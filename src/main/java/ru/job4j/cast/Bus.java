package ru.job4j.cast;

/**
 * Класс {@code Bus} представляет собой транспортное средство – автобус.
 *
 * <p>Используется для моделирования дорожного транспорта общего назначения.</p>
 *
 * <p><b>Пример использования:</b></p>
 * <pre>{@code
 * Vehicle bus = new Bus();
 * bus.type();
 * bus.move();
 * }</pre>
 *
 * <p><b>Пример вывода:</b></p>
 * <pre>{@code
 * Это автобус.
 * Автобус едет по скоростным трассам.
 * }</pre>
 *
 * @author Maksim Merkulov
 * @version 1.1
 */
public class Bus implements Vehicle {

    /**
     * Выводит информацию о типе транспортного средства.
     *
     * <p>Сообщает, что экземпляр является автобусом.</p>
     */
    @Override
    public void type() {
        System.out.println("Это автобус.");
    }

    /**
     * Описывает, как автобус перемещается.
     *
     * <p>Выводит информацию о движении автобуса по скоростным трассам.</p>
     */
    @Override
    public void move() {
        System.out.println("Автобус едет по скоростным трассам.");
    }
}
