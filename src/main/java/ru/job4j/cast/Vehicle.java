package ru.job4j.cast;

/**
 * Интерфейс {@code Vehicle} представляет собой абстракцию транспортного средства.
 *
 * <p>Используется для задания общего поведения всех транспортных средств,
 * таких как движение и идентификация типа транспорта.</p>
 *
 * <p><b>Пример использования:</b></p>
 * <pre>{@code
 * Vehicle plane = new Plane();
 * plane.type();
 * plane.move();
 * }</pre>
 *
 * <p><b>Пример вывода:</b></p>
 * <pre>{@code
 * Это самолет.
 * Самолет летит по воздуху.
 * }</pre>
 *
 * @author Maksim Merkulov
 * @version 1.1
 */
public interface Vehicle {

    /**
     * Показывает тип транспортного средства.
     *
     * <p>Реализация по умолчанию выводит "Транспортное средство".</p>
     */
    default void type() {
        System.out.println("Транспортное средство");
    }

    /**
     * Выполняет движение транспортного средства.
     *
     * <p>Каждая реализация определяет свою логику передвижения.</p>
     */
    void move();
}
