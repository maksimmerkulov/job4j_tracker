package ru.job4j.cast;

/**
 * Интерфейс {@code Animal} представляет собой абстракцию животного.
 *
 * <p>Используется для описания поведения животных, в частности — издаваемых ими звуков.</p>
 *
 * <p><b>Пример использования:</b></p>
 * <pre>{@code
 * Animal cow = new Cow();
 * cow.sound();
 * }</pre>
 *
 * @author Maksim Merkulov
 * @version 1.0
 * @since 2025-05-01
 */
public interface Animal {

    /**
     * Воспроизводит звук, характерный для конкретного животного.
     *
     * <p>Реализация метода зависит от вида животного.</p>
     */
    void sound();
}