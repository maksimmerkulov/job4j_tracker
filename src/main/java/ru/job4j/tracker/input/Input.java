package ru.job4j.tracker.input;

/**
 * Интерфейс {@code Input} определяет контракт для получения ввода от пользователя.
 *
 * <p>Может использоваться для различных реализаций: консольный ввод, ввод из файла, тестовые заглушки и т.п.</p>
 *
 * <p><b>Сценарии использования:</b></p>
 * <ul>
 *   <li>Запрос строки у пользователя через {@link #askStr}</li>
 *   <li>Запрос целочисленного значения через {@link #askInt}</li>
 * </ul>
 *
 * <p>Интерфейс применяется в приложении трекера заявок для взаимодействия с пользователем.</p>
 *
 * @author Maksim Merkulov
 * @version 1.2
 */
public interface Input {

    /**
     * Запрашивает строку у пользователя.
     *
     * @param question текст вопроса, отображаемый пользователю
     * @return введенная строка
     */
    String askStr(String question);

    /**
     * Запрашивает целое число у пользователя.
     *
     * @param question текст вопроса, отображаемый пользователю
     * @return введенное число
     */
    int askInt(String question);
}
