package ru.job4j.tracker;

import java.util.Arrays;

/**
 * Класс {@code Tracker} представляет собой хранилище для объектов типа {@link Item}.
 *
 * <p>Предназначен для управления заявками: добавление, получение списка всех заявок,
 * поиск заявок по имени или по уникальному идентификатору (ID), а также возможность замены заявки по ID.</p>
 *
 * <p><b>Основные возможности класса:</b></p>
 * <ul>
 *     <li>Добавление новой заявки с уникальным идентификатором.</li>
 *     <li>Получение списка всех добавленных заявок.</li>
 *     <li>Поиск заявок по имени с использованием метода {@code equals()} для сравнения строк.</li>
 *     <li>Поиск заявки по уникальному идентификатору {@code id}.</li>
 *     <li>Замена заявки по уникальному идентификатору {@code id}.</li>
 * </ul>
 *
 * <p>Каждой заявке автоматически присваивается уникальный идентификатор {@code id}, начиная с 1,
 * который увеличивается при добавлении каждого нового элемента. Количество заявок ограничено размером массива,
 * который по умолчанию равен 100 элементам.</p>
 *
 * @author Maksim Merkulov
 * @version 1.1
 * @since 2025-04-22
 */
public class Tracker {

    /**
     * Массив {@code items} хранит все добавленные заявки.
     * Максимальная вместимость массива — 100 элементов.
     * Заявки хранятся в порядке их добавления.
     */
    private final Item[] items = new Item[100];

    /**
     * Поле {@code ids} используется для генерации уникальных идентификаторов для заявок.
     * Начальное значение — 1, оно увеличивается при добавлении каждой новой заявки.
     */
    private int ids = 1;

    /**
     * Поле {@code size} хранит количество заявок, добавленных в хранилище.
     * Используется для отслеживания текущего заполнения массива {@code items}.
     */
    private int size = 0;

    /**
     * Метод {@code add(Item item)} добавляет новую заявку в хранилище.
     * Присваивает заявке уникальный идентификатор {@code id}, начиная с 1, и увеличивает счетчик {@code ids}.
     *
     * @param item Объект {@link Item}, который необходимо добавить в хранилище.
     * @return Добавленный объект {@link Item}, с присвоенным уникальным идентификатором {@code id}.
     */
    public Item add(Item item) {
        item.setId(ids++);
        items[size++] = item;
        return item;
    }

    /**
     * Метод {@code findAll()} возвращает массив всех заявок, добавленных в хранилище.
     *
     * <p>Массив не содержит {@code null}-значений и имеет длину, равную количеству добавленных заявок.</p>
     *
     * @return Массив всех добавленных заявок.
     */
    public Item[] findAll() {
        return Arrays.copyOf(items, size);
    }

    /**
     * Метод {@code findByName(String key)} возвращает массив заявок с указанным именем.
     *
     * <p>Поиск выполняется с использованием метода {@code equals()} для сравнения строк.</p>
     *
     * @param key Имя, по которому осуществляется поиск заявок.
     * @return Массив заявок, имя которых совпадает с {@code key}.
     * Если заявок с таким именем нет, возвращается пустой массив.
     */
    public Item[] findByName(String key) {
        Item[] rsl = new Item[size];
        int count = 0;
        for (int index = 0; index < size; index++) {
            if (items[index].getName().equals(key)) {
                rsl[count++] = items[index];
            }
        }
        return Arrays.copyOf(rsl, count);
    }

    /**
     * Метод {@code findById(int id)} выполняет поиск заявки по уникальному идентификатору {@code id}.
     *
     * <p>Метод ищет заявку в массиве {@code items} по ее идентификатору {@code id}.
     * Если заявка с указанным идентификатором найдена, возвращается соответствующий объект {@link Item}.
     * В противном случае возвращается {@code null}.</p>
     *
     * @param id Уникальный идентификатор заявки, по которому осуществляется поиск.
     * @return Объект {@link Item}, если заявка с указанным {@code id} найдена, иначе {@code null}.
     */
    public Item findById(int id) {
        int index = indexOf(id);
        return index != -1 ? items[index] : null;
    }

    /**
     * Метод {@code replace(int id, Item item)} заменяет заявку с указанным {@code id} на новую.
     *
     * <p>Если заявка с данным идентификатором найдена, то:</p>
     * <ul>
     *     <li>Новому объекту {@code item} присваивается тот же {@code id}, что и у заменяемой заявки.</li>
     *     <li>Существующая заявка заменяется на новую в массиве {@code items}.</li>
     * </ul>
     *
     * <p>Если заявка с указанным {@code id} не найдена, операция не выполняется.</p>
     *
     * @param id Уникальный идентификатор заявки, которую необходимо заменить.
     * @param item Новый объект {@link Item}, который подставляется вместо существующей заявки.
     * @return {@code true}, если заявка найдена и замена выполнена; {@code false}, если заявка не найдена.
     */
    public boolean replace(int id, Item item) {
        int index = indexOf(id);
        if (index != -1) {
            item.setId(id);
            items[index] = item;
            return true;
        }
        return false;
    }

    /**
     * Метод {@code indexOf(int id)} выполняет поиск индекса заявки в массиве {@code items}
     * по уникальному идентификатору {@code id}.
     *
     * <p>Метод ищет заявку с заданным идентификатором в массиве и возвращает индекс найденного элемента.
     * Если заявка не найдена, метод возвращает {@code -1}.</p>
     *
     * @param id Уникальный идентификатор заявки, по которому выполняется поиск.
     * @return Индекс найденной заявки в массиве {@code items}, если заявка найдена, иначе {@code -1}.
     */
    private int indexOf(int id) {
        int result = -1;
        for (int index = 0; index < size; index++) {
            if (items[index].getId() == id) {
                result = index;
                break;
            }
        }
        return result;
    }
}